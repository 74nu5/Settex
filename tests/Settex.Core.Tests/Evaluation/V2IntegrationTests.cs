namespace Settex.Core.Tests.Evaluation;

using System.Threading.Tasks;
using Settex.Core.Lexer;
using Settex.Core.Parser;
using Settex.Core.Evaluation;
using TUnit.Core;

/// <summary>
///     Integration tests for complete V2 features working together.
///     These tests validate that all V2 features work correctly in combination.
/// </summary>
public class V2IntegrationTests
{
    /// <summary>
    ///     Test complete V2 scenario: variables + expressions + for loops + if inline + :=
    /// </summary>
    [Test]
    public async Task CompleteV2Scenario_AllFeaturesWorking()
    {
        var source = """
            let basePort = 8000
            let host = "localhost"
            let authPort = 8001
            let apiPort = 8002
            
            settings {
                ApplicationName = "IntegrationTest"
                BaseUrl = "http://${host}:${basePort}"
                
                # Set-if-missing operator
                Server {
                    Port := 8080
                    MaxConnections := 100
                }
                
                # For loop generating services
                Services = [
                    for port in [authPort, apiPort] {
                        item {
                            Port = port
                            Url = "http://${host}:${port}"
                            Enabled = true
                        }
                    }
                ]
            }
            
            env "Development" {
                let devPort = 5000
                
                settings {
                    Server {
                        Port = devPort
                    }
                    BaseUrl = "http://dev.${host}:${devPort}"
                }
            }
            
            env "Production" {
                let prodPort = 443
                let prodHost = "api.example.com"
                
                settings {
                    Server {
                        Port = prodPort
                        MaxConnections = 1000
                    }
                    BaseUrl = "https://${prodHost}"
                }
            }
            """;

        var lexer = new Lexer(source, "test.settex");
        var tokens = lexer.Tokenize();
        var parser = new Parser(tokens);
        var ast = parser.Parse();
        var evaluator = new Evaluator();
        var model = evaluator.Evaluate(ast);

        // Check base settings
        await Assert.That(model.BaseSettings["ApplicationName"]?.GetValue<string>()).IsEqualTo("IntegrationTest");
        await Assert.That(model.BaseSettings["BaseUrl"]?.GetValue<string>()).IsEqualTo("http://localhost:8000");
        await Assert.That(model.BaseSettings["Server"]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(8080);
        await Assert.That(model.BaseSettings["Server"]?.AsObject()["MaxConnections"]?.GetValue<long>()).IsEqualTo(100);
        
        // Check services generated by for loop
        var services = model.BaseSettings["Services"]?.AsArray();
        await Assert.That(services).IsNotNull();
        await Assert.That(services!.Count).IsEqualTo(2);
        
        var authService = services[0]?.AsObject();
        await Assert.That(authService!["Port"]?.GetValue<long>()).IsEqualTo(8001);
        await Assert.That(authService["Url"]?.GetValue<string>()).IsEqualTo("http://localhost:8001");
        
        // Check Development environment
        var devSettings = model.EnvironmentOverlays["Development"];
        await Assert.That(devSettings["Server"]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(5000);
        await Assert.That(devSettings["BaseUrl"]?.GetValue<string>()).IsEqualTo("http://dev.localhost:5000");
        
        // Check Production environment
        var prodSettings = model.EnvironmentOverlays["Production"];
        await Assert.That(prodSettings["Server"]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(443);
        await Assert.That(prodSettings["BaseUrl"]?.GetValue<string>()).IsEqualTo("https://api.example.com");
        await Assert.That(prodSettings["Server"]?.AsObject()["MaxConnections"]?.GetValue<long>()).IsEqualTo(1000);
    }

    /// <summary>
    ///     Test nested for loops with expressions.
    /// </summary>
    [Test]
    public async Task NestedForLoops_WithExpressions()
    {
        var source = """
            let environments = ["dev", "staging", "prod"]
            let ports = [8000, 8001]
            
            settings {
                Endpoints = [
                    for e in environments {
                        item {
                            Environment = e
                            Services = [
                                for port in ports {
                                    item {
                                        Port = port
                                        Url = "http://${e}.example.com:${port}"
                                    }
                                }
                            ]
                        }
                    }
                ]
            }
            """;

        var lexer = new Lexer(source, "test.settex");
        var tokens = lexer.Tokenize();
        var parser = new Parser(tokens);
        var ast = parser.Parse();
        var evaluator = new Evaluator();
        var model = evaluator.Evaluate(ast);

        var endpoints = model.BaseSettings["Endpoints"]?.AsArray();
        await Assert.That(endpoints).IsNotNull();
        await Assert.That(endpoints!.Count).IsEqualTo(3);
        
        var devEndpoint = endpoints[0]?.AsObject();
        await Assert.That(devEndpoint!["Environment"]?.GetValue<string>()).IsEqualTo("dev");
        
        var devServices = devEndpoint["Services"]?.AsArray();
        await Assert.That(devServices!.Count).IsEqualTo(2);
        await Assert.That(devServices[0]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(8000);
        await Assert.That(devServices[0]?.AsObject()["Url"]?.GetValue<string>()).IsEqualTo("http://dev.example.com:8000");
    }

    /// <summary>
    ///     Test complex expressions with multiple operators.
    /// </summary>
    [Test]
    public async Task ComplexExpressions_MultipleOperators()
    {
        var source = """
            let a = 10
            let b = 5
            let c = 2
            let enabled = true
            let name = "test"
            
            settings {
                # Arithmetic with precedence
                Result1 = a + b * c - 3
                Result2 = a + b * c
                
                # Logical expressions
                Logic1 = enabled and a > b
                Logic2 = not enabled or b < c
                
                # Comparison chains
                Compare1 = a >= b and b > c
                Compare2 = a == 10 and name == "test"
                
                # Null coalescing
                Coalesce1 = null ?? "default"
                Coalesce2 = name ?? "fallback"
                
                # Mixed expressions
                Mixed = a > b and enabled or c == 2
            }
            """;

        var lexer = new Lexer(source, "test.settex");
        var tokens = lexer.Tokenize();
        var parser = new Parser(tokens);
        var ast = parser.Parse();
        var evaluator = new Evaluator();
        var model = evaluator.Evaluate(ast);

        await Assert.That(model.BaseSettings["Result1"]?.GetValue<long>()).IsEqualTo(17); // 10+5*2-3 = 10+10-3 = 17
        await Assert.That(model.BaseSettings["Result2"]?.GetValue<long>()).IsEqualTo(20); // 10+5*2 = 10+10 = 20
        await Assert.That(model.BaseSettings["Logic1"]?.GetValue<bool>()).IsEqualTo(true);
        await Assert.That(model.BaseSettings["Logic2"]?.GetValue<bool>()).IsEqualTo(false);
        await Assert.That(model.BaseSettings["Compare1"]?.GetValue<bool>()).IsEqualTo(true);
        await Assert.That(model.BaseSettings["Compare2"]?.GetValue<bool>()).IsEqualTo(true);
        await Assert.That(model.BaseSettings["Coalesce1"]?.GetValue<string>()).IsEqualTo("default");
        await Assert.That(model.BaseSettings["Coalesce2"]?.GetValue<string>()).IsEqualTo("test");
        await Assert.That(model.BaseSettings["Mixed"]?.GetValue<bool>()).IsEqualTo(true);
    }

    /// <summary>
    ///     Test set-if-missing with environment overlays.
    /// </summary>
    [Test]
    public async Task SetIfMissing_WithEnvironmentOverlays()
    {
        var source = """
            settings {
                Server.Host := "localhost"
                Server.Port := 8080
                Server.Timeout = 30
                
                Database.Host := "localhost"
                Database.Port := 5432
            }
            
            env "Production" {
                settings {
                    Server.Host = "api.example.com"
                    Server.Port := 443
                    
                    # These won't override base because base has them
                    Database.Host := "prod-db.example.com"
                    Database.Port := 5433
                }
            }
            """;

        var lexer = new Lexer(source, "test.settex");
        var tokens = lexer.Tokenize();
        var parser = new Parser(tokens);
        var ast = parser.Parse();
        var evaluator = new Evaluator();
        var model = evaluator.Evaluate(ast);

        // Base settings
        await Assert.That(model.BaseSettings["Server"]?.AsObject()["Host"]?.GetValue<string>()).IsEqualTo("localhost");
        await Assert.That(model.BaseSettings["Server"]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(8080);
        await Assert.That(model.BaseSettings["Database"]?.AsObject()["Host"]?.GetValue<string>()).IsEqualTo("localhost");
        await Assert.That(model.BaseSettings["Database"]?.AsObject()["Port"]?.GetValue<long>()).IsEqualTo(5432);
        
        // Production overlay
        var prodSettings = model.EnvironmentOverlays["Production"];
        await Assert.That(prodSettings["Server"]?.AsObject()["Host"]?.GetValue<string>()).IsEqualTo("api.example.com");
        
        // Server.Port := 443 should not set because base already has Server.Port
        await Assert.That(prodSettings.ContainsKey("Server")).IsTrue();
        var serverObj = prodSettings["Server"]?.AsObject();
        await Assert.That(serverObj!.ContainsKey("Port")).IsFalse(); // Not set in overlay because base has it
        
        // Database.Host and Port should not be set because base has them
        await Assert.That(prodSettings.ContainsKey("Database")).IsFalse();
    }

    /// <summary>
    ///     Test string interpolation with complex expressions.
    /// </summary>
    [Test]
    public async Task StringInterpolation_ComplexExpressions()
    {
        var source = """
            let host = "example.com"
            let port = 8080
            let protocol = "https"
            let version = 2
            
            settings {
                SimpleUrl = "${protocol}://${host}:${port}"
                VersionedUrl = "${protocol}://${host}:${port}/v${version}"
                ExpressionUrl = "${protocol}://${host}:${port + 100}"
                NestedExpr = "Version ${version + 1} on ${protocol}://${host}"
            }
            """;

        var lexer = new Lexer(source, "test.settex");
        var tokens = lexer.Tokenize();
        var parser = new Parser(tokens);
        var ast = parser.Parse();
        var evaluator = new Evaluator();
        var model = evaluator.Evaluate(ast);

        await Assert.That(model.BaseSettings["SimpleUrl"]?.GetValue<string>()).IsEqualTo("https://example.com:8080");
        await Assert.That(model.BaseSettings["VersionedUrl"]?.GetValue<string>()).IsEqualTo("https://example.com:8080/v2");
        await Assert.That(model.BaseSettings["ExpressionUrl"]?.GetValue<string>()).IsEqualTo("https://example.com:8180");
        await Assert.That(model.BaseSettings["NestedExpr"]?.GetValue<string>()).IsEqualTo("Version 3 on https://example.com");
    }
}
